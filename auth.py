    login_time = datetime.utcnow().isoformat()
    st.sidebar.markdown(f"ðŸ‘¤ **{name}** ({username})")
    st.sidebar.caption(f"ðŸ”’ Session started: {login_time[:16]} UTC")
    return login_time
